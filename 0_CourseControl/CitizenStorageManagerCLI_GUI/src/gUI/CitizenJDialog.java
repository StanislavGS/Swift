    private void jButton3MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseReleased

        try {
            dataStore = new DataStore(jTextField1.getText());
            dataStore.fillObjects();
            jLabel1.setText("Recorded "
                    + dataStore.getCitizensCount()+" citizens in memory.");
            if (dataStore.getCitizensCount()>0){
                jButton4.setEnabled(true);
            }
            
        } catch (IOException | DataReadException ex) {
            JOptionPane.showMessageDialog(null, "Error while reading file!" 
                    , "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton3MouseReleased

    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        MySQLCitizenStorage citizenDB=new MySQLCitizenStorage();
        try {
            citizenDB.clearWholeDB();
            jLabel2.setText("There is 0 citizens records in DB");
        } catch (DALException ex) {
            JOptionPane.showMessageDialog(null, "Error connect DB!" + ex.getdALmessage()
                    , "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton7MouseClicked

    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        if (dataStore==null || dataStore.getCitizensCount()==0){
            return;
        }
        try {
            dataStore.putToDB();
        } catch (DALException ex) {
            JOptionPane.showMessageDialog(null, "Error putting in DB!" + ex.getdALmessage()
                    , "Error", JOptionPane.ERROR_MESSAGE);
        }
        jLabel2.setText("There is "+getCitizensCount()+" citizens records in DB");
        jLabel1.setText("There is 0 records in memory.");
    }//GEN-LAST:event_jButton4MouseClicked

