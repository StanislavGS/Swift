    private int getCitizensCount(){
        MySQLCitizenStorage citizenDB=new MySQLCitizenStorage();
        int result=0;
        try {
            result= citizenDB.getCitizensCount();
        } catch (DALException ex) {
            JOptionPane.showMessageDialog(null, "Error connect DB!" + ex.getMessage()
                    , "Error", JOptionPane.ERROR_MESSAGE);
        }
        return result;
    }
    
